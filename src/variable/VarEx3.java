package variable;

public class VarEx3 {
    public static void main(String[] args) {
        /*
        long 타입의 변수를 선언하고 그 변수를 10000000000으로 초기화한 후 출력
        boolean 타입의 변수를 선언하고 true로 초기화한 후 출력
         */

        long l = 10000000000L;
        System.out.println("l = " + l);

        boolean b = true;
        System.out.println("b = " + b);

    }
}
