package variable;

public class VarEx1 {
    public static void main(String[] args) {
        /*
        반복해서 나오는 숫자 4, 3을 변수 num1, num2를 사용하도록 변경하기

        System.out.println(4 + 3);
        System.out.println(4 - 3);
        System.out.println(4 * 3);
         */

        int num1 = 4;
        int num2 = 3;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
    }
}
