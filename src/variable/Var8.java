package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127
        short s = 32767; // -32,768 ~ 32,767
        int i = 2147483647; // 약 20억
        long l = 9223372036854775807L;

        // 실수
        float f = 10.0f;
        double d = 10.0;

        /*
        메모리 용량은 저렴하기 때문에 용량을 약간 절약하기 보다 개발 속도나 효율에 초점을 맞추는 것이 더 효과적
         */
    }
}
